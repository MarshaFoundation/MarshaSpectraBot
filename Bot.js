const TelegramBot = require('node-telegram-bot-api');
const i18n = require('i18n');
const wtf = require('wtf_wikipedia');
const axios = require('axios');
require('dotenv').config();

const token = process.env.TELEGRAM_API_KEY;
const openaiApiKey = process.env.OPENAI_API_KEY;

// ConfiguraciÃ³n del objeto de configuraciÃ³n
const CONFIG = {
    locales: ['en', 'es'],
    defaultLocale: 'es',
    cacheMaxSize: 100,
    openaiApiUrl: 'https://api.openai.com/v1/chat/completions',
    gptModel: 'gpt-3.5-turbo',
    responseTemperature: 0.7,
};

i18n.configure({
    locales: CONFIG.locales,
    directory: __dirname + '/locales',
    defaultLocale: CONFIG.defaultLocale,
    queryParameter: 'lang',
    cookie: 'locale',
});

const bot = new TelegramBot(token, { polling: true });
console.log('Bot iniciado correctamente');

// ImplementaciÃ³n de una cachÃ© LRU
class LRUCache {
    constructor(maxSize) {
        this.maxSize = maxSize;
        this.cache = new Map();
    }

    get(key) {
        if (!this.cache.has(key)) {
            return null;
        }
        const value = this.cache.get(key);
        this.cache.delete(key);
        this.cache.set(key, value);
        return value;
    }

    set(key, value) {
        if (this.cache.size >= this.maxSize) {
            const keys = this.cache.keys();
            this.cache.delete(keys.next().value);
        }
        this.cache.set(key, value);
    }
}

const cache = new LRUCache(CONFIG.cacheMaxSize);

// FunciÃ³n para hacer la llamada a OpenAI con control de frecuencia
async function getChatGPTResponse(messages) {
    const messagesKey = JSON.stringify(messages);
    const cachedResponse = cache.get(messagesKey);
    if (cachedResponse) {
        return cachedResponse;
    }

    try {
        const response = await axios.post(CONFIG.openaiApiUrl, {
            model: CONFIG.gptModel,
            messages: messages,
            temperature: CONFIG.responseTemperature,
        }, {
            headers: {
                'Content-Type': 'application/json',
                'Authorization': `Bearer ${openaiApiKey}`
            }
        });

        const gptResponse = response.data.choices[0].message.content.trim();
        cache.set(messagesKey, gptResponse);

        return gptResponse;
    } catch (error) {
        console.error('Error al llamar a OpenAI:', error);
        return 'Lo siento, actualmente no puedo procesar tu solicitud.';
    }
}

async function handleError(chatId, errorMessage, errorDetails = '') {
    console.error(errorMessage, errorDetails);
    await bot.sendMessage(chatId, i18n.__('Ha ocurrido un error. Por favor, intÃ©ntalo nuevamente mÃ¡s tarde.'));
}

